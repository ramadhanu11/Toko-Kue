    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        if(idKonsumen.getText().equals("") || 
                namaKonsumen.getText().equals("") || 
                telpKonsumen.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Silahkan Pilih Data Terlebih Dahulu Pada Tabel");
        }
        
        else {
            int option = JOptionPane.YES_NO_OPTION;
            int optionconfirm = JOptionPane.showConfirmDialog(this, "Apa Anda Yakin Ingin Mengedit Data Konsumen?", "Konfirmasi", option);
            if(optionconfirm == 0) {
                try {
                    con = Koneksi.getKoneksi();
                    String sql = "UPDATE pelanggan set id_konsumen='"+idKonsumen.getText()
                            +"',nama_konsumen='"+namaKonsumen.getText()
                            +"',telp_konsumen='"+telpKonsumen.getText()
                            +"',alamat_konsumen='"+alamat.getText()
                            +"' where id_konsumen='"+idKonsumen.getText()+"'";
                    st = con.createStatement();
                    st.execute(sql);
                    
                    JOptionPane.showMessageDialog(this, "Data Konsumen Berhasil Diupdate");
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.setRowCount(0);
                    data();
                    clear();
                    namaKonsumen.requestFocus();
                }
                catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }    
    }//GEN-LAST:event_editMouseClicked

