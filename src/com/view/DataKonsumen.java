    // MENAMPILKAN TABEL KONSUMEN
    private void data() {
        try {
            con = Koneksi.getKoneksi();
            String sql = "SELECT * FROM pelanggan ORDER BY id_konsumen ASC";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()){
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                Object[] row = new Object[4];
                row[0] = rs.getString("id_konsumen");
                row[1] = rs.getString("nama_konsumen");
                row[2] = rs.getString("telp_konsumen");
                row[3] = rs.getString("alamat_konsumen");
                model.addRow(row);
            }
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
//    AUTO INCREMENT
    private void increment(){
        try{
            con = Koneksi.getKoneksi();
            String sql = "SELECT COUNT(id_konsumen) as id FROM pelanggan";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()){
                String kode = rs.getString("id");
                int a = Integer.parseInt(kode);
                int b = a+1;
                idKonsumen.setText("PJ2022"+b);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    private void cari(String searchString){
        originalTableModel = (Vector) ((DefaultTableModel) jTable1.getModel()).getDataVector().clone();
        DefaultTableModel currtableModel = (DefaultTableModel) jTable1.getModel();
        //To empty the table before search
        currtableModel.setRowCount(0);
        //To search for contents from original table content
        for (Object rows : originalTableModel) {
            Vector rowVector = (Vector) rows;
            for (Object column : rowVector) {
                if (column.toString().toLowerCase().contains(searchString)
                        | column.toString().toUpperCase().contains(searchString)
                        | column.toString().contains(searchString)) {
                    //content found so adding to table
                    currtableModel.addRow(rowVector);
                    break;
                }
            }
        }if(CariData.getText().equals("")){
            currtableModel.setRowCount(0);
            data();
        }
    }
    
    private void clear(){
        idKonsumen.setText("");
        increment();
        namaKonsumen.setText("");
        telpKonsumen.setText("");
        alamat.setText("");
    }
